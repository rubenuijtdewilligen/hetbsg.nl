  <h2 class="my-2 text-xl font-bold">Brabantia Nostra</h2>
  <p>
    De leidende rol voor academici, zoals die was geformuleerd in de richtlijnen van 1930, lag ten
    grondslag aan een voorstel in datzelfde jaar om een brochurereeks uit te geven over de historie
    van Brabant. Dit initiatief kwam van Geert Ruygers, student Nederlandse letteren aan de
    Katholieke Leergangen te Tilburg. Dr. P.C. de Brouwer had de naam al bedacht: 'Brabantia
    Nostra', wat niet oubollig als 'Ons Brabant' moest worden verstaan, maar als een strijdkreet:
    'Brabant aan ons!' De niet-Brabander Joop Meijers, student in Utrecht, wist het Gilde ervan te
    overtuigen dat een brochurenreeks te elitair was en dat het beter was een tijdschrift, liefst
    een weekblad, uit te geven. De verwezenlijking van het idee liet op zich wachten. Het vertrek
    van de Van Santianen in 1933 betekende een verschuiving van de rol van het Gilde richting het
    dienende standpunt. De uitgave van het tijdschrift werd echter volledig gesteund door Dr. de
    Brouwer en Siemer. Hun belangrijkste drijfveer was vooral het idee dat het katholieke Brabantse
    volk tot bewustwording moest worden gebracht, waarbij de jonge intellectuelen zich ter
    beschikking moesten stellen. De gedachtenwereld van het Gilde bleek, na afstand te hebben
    genomen van incidentele nationalistische en politieke aangroeisels, nu geschikt om het idée
    directrice van een nieuwe institution voort te brengen (1). Uit de propaganda-acties voor het
    blad groeide in de loop van het jaar 1938 (officieel pas 1 januari 1939) een 'Beweging Brabantia
    Nostra', die o.a. 'verantwoordelijk' is voor de bouw van de Mariakapel aan de nieuwe
    Moerdijkverkeersbrug. In de zomer van 1942, toen aansluiting bij de Kultuurkamer dreigde en
    Frans van der Ven met Jef de Brouwer werden gegijzeleld, werd besloten de uitgave van het blad
    te staken. Na de oorlog fuseerde het heropgerichte Brabantia Nostra met een 'Brabantse Beweging'
    en gaf ze het populaire gezinsblad 'Edele Brabant' uit. Maar ook dit was geen lang leven
    beschoren. De enige concrete naoorlogse activiteiten waren de Groot-Kempische Cultuurdagen te
    Hilvarenbeek en het Brabants Halfuur op de KRO-radio. In 1950-51 kwam Brabantia Nostra weer als
    blad uit, maar na ‚‚n jaargang ging het noodgedwongen samen met het 'Provinciaal Genootschap'
    over in 'Brabantia', dat nog steeds bestaat. De enige keren dat Brabantia Nostra nog wat van
    zich liet horen was door het organiseren van congressen in de jaren 60 (2).
  </p>
  <h2 class="my-2 text-xl font-bold">De lotgevallen van het BSG in de jaren 1940-1945</h2>
  <p>
    Na de capitulatie van Nederland in mei 1940 begrepen weinigen wat voor gevolgen dit kon hebben.
    Langzaam herstelde het leven van alledag zich en zo kon het ook gebeuren, dat het Brabants
    Studenten Gilde in augustus 1940 haar jaarlijkse kamp hield in Eersel. Ook nu werd een kapel
    gebouwd, inleidingen gehouden, gegeten en gepraat, geschertst en gedronken. Ongestoord werden
    nationale liederen gezongen, feller dan normaal. Men rekende er echt op dat het jaar daarop weer
    een kamp werd gehouden in een vrij Nederland. Ook toen er van hogerhand steeds meer restricties
    werden opgelegd, en bijeenkomsten van meer dan twintig personen aangevraagd moesten worden, wist
    het Gilde van geen ophouden. Zelfs niet toen, na negering van dit gebod de praeses en enkele
    leden hadden vastgezeten. De traditie van het Gilde moest worden voortgezet, zij het
    voorzichtig. Er kwamen zelfs nog nieuwe ideeën op tafel. Het volgend jaar zou op het kamp een
    sociografisch onderzoek ingesteld worden naar een streek van Brabant. De studenten zouden de
    toestand van de streek beschrijven, en zo het contact met de bevolking bevorderden. De gevaren
    van dat moment beletten echter de uitvoering van het plan. Op het kamp in Beers, op het landgoed
    van Prof. de Quay, werd in twee weken een kapel gebouwd van materiaal, aangevraagd voor de bouw
    van een boerenschuurtje. Studenten stonden nog niet als 'Deutsch-feindlich' bekend; de grote
    studentenstrijd was nog niet ontbrand. Die kwam aan het einde van dat jaar, 1942, en in begin
    1943. In februari 1943 werden de eerste studenten door de bezetter naar Vught gebracht en werd
    het Gilde volledig in dienst van de studentenstrijd gemobiliseerd. Het verkrijgen van informatie
    over alle ontwikkelingen was voor de studenten nu van het allergrootste belang om deel te kunnen
    nemen in de zo ongewone strijd. Velen waren echter sinds de Vught-affaire thuis gebleven, of op
    het platteland ondergedoken. In het bereiken van de medestudenten was een taak weggelegd voor
    het Gilde. Er werden eind februari studenten bijeengeroepen, die de taak op zich namen in
    Brabant alle studenten met berichten, Geuzen e.d. te bereiken. Teneinde onder alle
    omstandigheden iedereen te kunnen bereiken werden in iedere plaats meisjes aange- zocht om zo
    nodig de taak van de studenten over te nemen. In mei 1943, toen deze contact- en verzetsgroep,
    een 'neven-organisatie' van het Gilde, op volle toeren ging werken, werden ook deze meisjes,
    'nichtjes', volledig ingeschakeld. Vanuit het BSG werkten dertig studenten en zeventien
    'nichtjes' werkten in de strijd tegen de bezetter. De contacten met de Nederlandse studenten in
    Duitsland verliepen ook via de nichtjes, die hen brieven en pakketjes bezorgden. De zorg voor de
    ondergedoken studenten kwam echter op de eerste plaats. De studenten streden op de hen eigen
    manier, ze boden in de beslissende maanden van begin 1943 een volledig, geestelijk verzet. De
    strijd kostte offers; van iedere student minstens tijd, energie en geld. Maar gelukkig heeft het
    Brabants Studenten Gilde van onze Lieve Vrouw niemand verloren als gevolg van zijn of haar werk.
    Inmiddels was de specifieke roeping van het Gilde niet vergeten. In augustus 1943 door een
    kleine groep studenten in Tilburg, op Moerle, een kapel gebouwd ter ere van Onze Lieve Vrouw van
    de Goeden Duik. Bovendien hield het bestuur contact met (oud-)leden van het Gilde om het
    naoorlogse (Gilde-)werk voor te bereiden. In 1944 werd door een dertigtal studenten een kapel in
    Heeswijk gebouwd, in een ongekende vrijheid en vooroorlogse sfeer. Op 15 augustus 1944 woonden
    een vijftigtal gasten de inzegening van de kapel bij. Op dezelfde dag was de heldere lucht
    bedekt met geallieerde vliegtuigen, die de vliegvelden van Nederland bombardeerden - voor
    Brabant een teken van het naderende einde van de strijd, en het verkrijgen van de zo lang
    verwachte vrijheid (3).
  </p>
  <h2 class="my-2 text-xl font-bold">Wederopbouw en vooruitgang</h2>
  <p>
    Het bestuur, dat sinds 1943 het Gilde leidde en in die tijd ook zo voortreffelijk het
    onderduiken en verzorgen van de naar het Zuiden van ons land gevluchte studenten op zich nam,
    vatte zijn eigenlijke taak (vorming van katholieke intellectuele Brabanders) na de bevrijding
    weer op. Het Gilde streefde er nu echter naar om niet meer voor als een kleine kern te werken,
    zoals tot dan toe onbedoeld was gebeurd, maar voor alle Brabantse studenten, opdat het Brabants
    Studenten Gilde van Onze Lieve Vrouw haar taak naar waarheid kon vervullen (4). Het leeuwendeel
    van de Brabantse studenten was vlak voor of tijdens de oorlog begonnen en was onvoldoende van
    het Gilde op de hoogte. Het was zaak hen te bereiken, maar de middelen hiervoor (circulaires)
    waren echter beperkt en lieten weinig ruimte om het BSG en de haar leidende idee uiteen te
    zetten. Er werd daarom besloten tot de uitgave van een brochure over het Gilde. In deze brochure
    werd ook het sociografisch werkkamp voor het eerst als Gilde-activiteit aangekondigd. Het Gilde
    wilde, behalve hen voor Brabant te behouden, de student ook voorbereiden voor de toekomst. Er
    moest derhalve gewaakt worden voor niet-realistisch sectarisme en provincialisme. Integratie
    werd derhalve gezien als een belangrijk onderdeel van de academische studentenorganisaties.
    Tijdens de trimesters aan de academie vonden er daarom weinig Gilde-activiteiten plaats. Want
    wanneer dit algemeen gedaan zou worden, hierbij denkend aan de Friesche Studentenbeweging, aan
    een Limburgs, een Twents en een Zeeuws Gilde, aan het Hollands Studenten Gilde van St. Jeroen en
    aan een Gilde in de Lijmers (Gelderland), die in die tijd werden (her)opgericht, dan zouden de
    academische studentenorganisaties enorm opgesplitst worden, met alle gevolgen vandien. Juist aan
    de academie bestond gelegenheid contact te leggen met studenten uit alle streken van ons land.
    Zo werd weer gewezen op het ene Nederland en werd men voor een gewestelijk chauvinisme behoed.
    Ook na de oorlog bleef de grootste activiteit van het Gilde het Zomerkamp, afgesloten met de
    Landdag der Brabantse studenten. Nieuwe hierin was dus het Sociografisch werkkamp, reeds in 42
    ontstaan maar door de bezetting nimmer verwezenlijkt. De gedachten achter het werkkamp waren als
    volgt. Voor velen was de Gilde-idee ideaal, maar het Gildewerk te abstract. In het Sociografisch
    werkkamp werd gelegenheid geschapen voor iedere student om naast de verstandelijke voorbereiding
    praktisch te werken in zijn eigen vak in het eigen gewest. Het verschil van de oude Zomerkampen
    met de nieuwe sociografisch werkkampen werd bijna volledig uitgedrukt in het verschil tussen de
    begrippen theorie en praktijk. Zelfs de opvatting van het Gilde over het contact met het volk
    werd nu bij het sociografisch enquˆte-werk pas realiteit. De Zomerkampen kregen onder invloed
    van de Sociografische kampen een wat ander karakter en leverde meer inzicht in bepaalde
    Brabantse problemen dan tot dan toe mogelijk was. Hiermee kon op het Brabantse platteland ook
    beter het vertrouwen in de toekomstige leidende stand gewonnen worden. De academici konden de
    basis scheppen voor de vorming van alle Brabanders, ieder naar aard, aanleg en stand. Brabant,
    de laatste tientallen jaren ontworsteld aan een toestand van onderont- wikkeling en armoede,
    voelde zich nog steeds achtergesteld. De deels gegronde reactie op deze achterstelling ging
    echter weer te ver. Men vergat bovendien de hand in eigen boezem te steken, de eigen fouten te
    signaleren en te trachten deze te verbeteren. De opkomende academische stand, die belangrijke
    posities in en buiten Brabant zouden gaan innemen, zou de achterstelling echter spoedig moeten
    doen verdwijnen. Het Gilde kon bijdragen aan de daadwerkelijke verheffing van Brabant en daarom
    was haar plaats temidden van andere bewegingen die naar deze verheffing streefden zo belangrijk.
    De Sociografische werkkampen werden over een lange periode uitgevoerd. Dat de uitkomsten
    behoorlijk in de buurt kwamen van de verwachtingen moge wel blijken uit het onderstaande artikel
    dat overgenomen is uit een Den Dungens blad uit 1949, alwaar in dat jaar het Zomerkamp werd
    gehouden: "...de bedoeling is dus dat de toekomstige intellectuele leiders van Brabant en het
    volk van Brabant elkaar goed leren kennen. Dit jaar is die opzet volledig geslaagd: in Den
    Dungen mag je geen kwaad woord meer zeggen van de studenten, want ze kennen hun losbandigheid en
    lichtzinnige streken nu van eigen aanschouwing, maar ook hun ernst en oprechtheid hebben ze
    leren waarderen. En de studenten kennen Den Dungen d.w.z. de meesters van de school, de
    ambtenaren van de gemeente, de boeren en tuinders, de smid en de timmerman, de kappers die
    tegelijk een tabakswinkel bedrijven, burgers en lieve burgeressen, zo goed als de geestelijke en
    de burgerlijke grootheden. En dit is heel belangrijk: leiders en volk hebben elkaar leren "ken-
    nen, waarderen en liefhebben". Auteur vindt dit laatste net zo belangrijk en bovendien veel
    hoopgevender dan de uitvoerige verslagen betreffende droevige politieke constellaties. Nochtans
    heeft de Volkskrant aan de hele soesah geen regel lood verspild. Het leven wordt immers gemaakt
    in de hoofdsteden, maar op een dorp? Wat goeds komt er uit Nazareth, nietwaar (5)?
  </p>
  <h2 class="my-2 text-xl font-bold">Aanpassing aan de tijd</h2>
  <p>
    In de zestiger jaren had BSG-moderator Bogers in geen weet van de BSG-statuten en werd hun
    bestaan zelfs sterk in twijfel getrokken. Dit blijkt uit het artikel in het verslag van het
    Paascongres van 1966, dat behalve deze misser een duidelijk beeld van het BSG en haar
    bestaansrecht uitdraagt: "Welke waren de bedoelingen van het Gilde? Waaruit en waarvoor bestond
    het Gilde? Hoe lagen deze bedoelingen vast in haar statuten? Hebben ze ooit bestaan of werden ze
    telkens opnieuw opgesteld en even prettig als nonchalant afgeleid uit wat zich voordeed? Het
    Gilde bloeit als een roos boven de afgrond, en het is met dit riskante bestaan vertrouwd
    geworden. Daarom vraagt het niet meer naar de zin van haar bestaan en wordt dus noch gefolterd
    door de wanhoop van de absurditeit, noch gesteund door de waanwijsheid van de onmisbaarheid. Het
    Gilde leeft, maar als iemand op hogeschool of universiteit vraagt, wat het in haar schild voert,
    kan men slechts antwoorden: Kom en zie. En dan zijn er niet zoveel die na een eerste
    kennismaking geen sympathie gaan voelen voor het samenzijn met zo weinig consequenties en zoveel
    warmte. Het bestaan zonder statuten waarborgt een uiterst snelle aanpassing aan de vragen van de
    tijd. Van augustus-kamp werd overgeschakeld naar Paaskamp. Van gezellig samenzijn met enkele
    lezingen en kapelbouw van weinigen, werd bewust overgeschakeld naar het sociologisch onderzoek.
    Het Gilde is zich langzaamaan bewust geworden van de mogelijkheid tot dienstbaar zijn in actuele
    behoeften. Het zal nooit de eigen behoefte vergeten, maar het zoekt in dialoog met de behoeften
    van een bepaalde plaats telkens haar eigen actuele vorm. Op de meest vanzelfsprekende wijze
    heeft het Gilde gebroken met de handhaving van een idyllisch Brabants bezit. Op een in deze tijd
    irriterende wijze komt in het oude Gilde-lied vier maal het woord 'hebben' voor, als een
    dominerende functie. Het 'hebben' camoufleert veel innerlijke leegte, zolang het de brug niet is
    naar het 'zijn'. Men voelt zich nu ge‹ntegreerd in de Nederlandse samenleving en heeft geen
    handhaving meer nodig. Men is ontvankelijk en dankbaar voor andere invloeden en voelt zich
    slechts Brabander - en dit is reële aanvaarding en erkentelijkheid - voorzover het hen helpt om
    beter Nederlander en wereldburger te zijn. Deze erkentelijkheid laat de renist zich bij het
    Gilde thuis voelen. Hij bevindt zich in zijn oude klimaat van de Brabantse aanspreekbaarheid,
    openheid en hartelijkheid (7)". In 1966 werd te Valkenswaard het achtste lustrum gevierd. Tevens
    sloeg het Gilde hier een nieuwe weg in. Voor het eerst werd er gebroken met het sociologisch
    onderzoek. In 1966 verschoof de nadruk weer meer richting de sociale en dienende rol van de
    student in de samenleving, geheel in overeenstemming met de tijdgeest. Het jubileumcongres werd
    in de vorm van een voorlichtingscongres gegoten: alle eindexaminandi van middelbare scholen,
    kweekscholen en hogere technische scholen van Eindhoven, Valkenswaard e.o. waren met het oog
    daarop uitgenodigd (5).
  </p>
  <h2 class="my-2 text-xl font-bold">Het einde?</h2>
  <p>
    In 1971 werd geen Paascongres gehouden: de omslachtige bestuursstructuur, waarbij de
    bestuursfuncties waren verdeeld over Tilburg, Nijmegen en Wageningen, leek het nog resterende
    enthousiasme de grond in te hebben geboord. Eind 1969 tekende het onheil zich al af. In Nijmegen
    brandde het vuur echter nog wel, getuige een brief van een Nijmeegs bestuurslid aan de quaestor,
    waarin de schrijver fel uithaalde tegen het voorstel van een mede-bestuurslid BSG met ingang van
    1 januari 1970 op te heffen. Hij stelde hierin ook voor - en wel zo gauw mogelijk - een
    bestuursvergadering te beleggen: "Misschien zouden we deze vergadering de vorm kunnen geven van
    een "retraite", waarin we ons bezinnen over het recht dat "wij" menen te hebben een vreugdevolle
    traditie al dan niet te beïndigen." Het Gilde was voorlopig gered. In 1970 werd vanuit de
    Nijmeegse gelederen een congres georganiseerd, ditmaal te Nistelrode. In augustus 1971 was men
    nog bezig met de financiële afhandeling van dit congres en, hoewel men stelde: "...dat dit
    congres heeft bewezen, dat het BSG op de goede weg is en alle reden heeft zich voor te bereiden
    op het 45e congres," bloedde het BSG-gebeuren dood (5).
  </p>
  <h2 class="my-2 text-xl font-bold">Reactivatie</h2>
  <p>
    Op 3 mei 1973 werd het BSG gereactiveerd vanuit Wageningen. Men besloot de in 1966 ingeslagen
    weg te volgen en stelde zich ten taak eens per jaar in de vorm van een congres de actuele
    problematiek van een bepaalde Brabantse gemeente samen met de inwoners te bediscussiëren. Na een
    aanlooptijd, gestructureerd door een coördinatiecommissie, werd op 13 november de macht van het
    oude bestuur "officieel" overgenomen door het nieuwe (Wageningse) bestuur. Buiten Wageningen
    hield het animo voor het BSG niet over, maar wellicht kon het Gilde ook daar via per- soonlijke
    benadering weer gaan leven. In Wageningen daarentegen liet men er geen gras over groeien. In
    oktober 1973 stond reeds vast dat Lieshout de komende congres-plaats zou worden. In januari 1974
    werd in Lieshout een informatiedag gehouden, waarna van 4 tot 7 april het Paascongres zich
    aldaar voltrok. Dit congres werd een onverdeeld succes. In de daarop volgende jaren ging het
    Gilde op dezelfde voet verder. De Gilde- doelstelling werd als volgt: "Het BSG stelt zich
    primair tot taak contacten te leggen tussen bewoners en studenten in Brabant, zodat beide
    groeperingen ervaringen kunnen uitwisselen waarbij een brede oriëntatie op het maatschappelijk
    gebeuren mogelijk wordt". De verschuiving van de primaire invalshoek van het Gilde van het
    "katholiek-zijn" uit 1930 naar het "breed oriënteren" van 1973 is niet geleidelijk verlopen. Uit
    de geschiedenis vallen twee grote stappen te onderscheiden: de overgang naar een praktische
    invulling van het contact met de bevolking via de sociografische kampen en de veranderingen in
    de jaren 60, 1966 in het bijzonder. De tijdelijke opheffing van het BSG met haar confessionele
    achtergrond past in het tijdsbeeld van de jaren zestig, waarin de ontzuiling en de
    ontkerkelijking een grote vlucht nam. Het BSG herrees in 1973 dan ook zonder haar confesionele
    inslag. Het "katholiek-zijn" was voortaan vooral een historisch onderdeel van het
    "Brabander-zijn". Het afwerpen van de geloofsmantel was terug te vinden in de naamsverandering
    van de hoofdactiviteit, die niet meer als "(Paas)Congres" werd betiteld, maar als "Landdagen van
    het BSG". Weliswaar na enige discussie, want "Landdagen" zou iets afstotends (NSB) in zich
    dragen. Opvallend is dat de opzet van deze Landdagen door de jaren heen weinig verschilt van de
    in 1974 ingeslagen weg. Uiteraard vindt men verschillen in de diverse programma-onderdelen, maar
    deze zijn veelal het gevolg van inspelingen op plaatselijke en toevallige omstandigheden. De
    Landdagen bevatten immer een aantal thema-avonden, waarvan er minstens één een agrarisch
    onderwerp behandelde, om zo aan te sluiten bij de plaatselijke bevolking.
  </p>
