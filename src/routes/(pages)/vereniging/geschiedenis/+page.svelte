  <h2 class="my-2 text-xl font-bold">Inleiding</h2>
  <p>
    Geschiedschrijving lijkt vaak geen waarde te bezitten. Het belang te weten op wiens schouders je
    staat wordt niet hoog ingeschat; men kijkt liever vooruit. In de geologie heeft men echter het
    inspirerende gezegde to study the past, to learn about the future. Ook op de menselijke
    tijdschaal is dit van toepassing. Het BSG is nooit groot geweest en de geschiedenis leert ons
    dat dit ook niet snel zal gebeuren. Zij leert ons ook dat, om te kunnen bestaan, er een
    bindmiddel nodig is. Dit bindmiddel werd lang gevormd door het katholieke geloof, terwijl de
    verzuiling het corset vormde dat het ondersteuning gaf. Toen het bindmiddel niet meer voldeed en
    in de jaren zestig het corset werd afgeworpen, bleek de wankele basis van het BSG daar niet
    tegen bestand. Slechts het enthousiasme en het geloof in een reden van voortbestaan heeft het
    BSG gered. Zonder doel is er geen reden van bestaan, zo lijkt het verleden aan de toekomst te
    leren. De nu volgende geschiedenis is gebouwd op de schouders van de vorige geschiedschrijvers
    en de archivarissen van het BSG, die de geschiedenis ook in vorige lustrumboeken tot in alle
    details hebben beschreven. Het is belangrijk dat deze uitgebreide geschiedenis aan eenieder
    doorgegeven blijft worden, omdat een besef van het verleden een zorg voor de toekomst waarborgt.
  </p>

  <h2 class="my-2 text-xl font-bold">De oprichting</h2>
  <p class="mb-2">
    In de lente van 1926 werd een oproep uitgezonden door Jules Froger en Cees Smelt om tijdens een
    'Brabantse Landdag' te Oirschot een Brabants Studenten Gilde mee te komen oprichten. Jules en
    Cees waren beiden student aan de T.H. Delft, katholiek en dùs lid van de Katholieke
    Studentenvereniging 'Sanctus Virgilius'. De zomerse Brabantse Landdag zou de gelegenheid bieden
    voor contacten tussen Brabantse, katholieke studenten onderling en met ons goede Brabantse volk.
    Een trefpunt als de Landdag was om twee redenen gewenst. Studeren boven de rivieren betekende
    dat men thuis tijdens de lange zomervakantie tamelijk geïsoleerd was; het maken van grote reizen
    was wegens de kosten daarvan toen nog allerminst gebruikelijk. En wat het contact met 'ons goede
    Brabantse volk' betreft; dat klonk ook wel aantrekkelijk.
  </p>
  <p>
    Het programma voor de eerste Brabantse Landdagen, in een dorp uiteraard, was in grote lijnen
    door traditie bepaald. Na de begroeting van de deelnemers door de burgemeester trok men op naar
    de Hoogmis (kwam dat niet overeen met het tijdschema, dan kon men met de pastoor nog altijd een
    plechtig Lof in de namiddag afspreken). Na de mis verspreidde het gezelschap zich over de
    cafeetjes totdat alles in gereedheid was gebracht voor de Brabantse koffietafel in de open
    lucht, waarbij het altijd goed was van eten en drinken'. Na de maaltijd kwam dan de echte Guld
    het marktplein op voor het sacrale spel van het vendelen. Het hoogtepunt moest dan nog komen: de
    vergadering met de feestredenaar. Op die eerste landdag in 1926 te Oirschot gebeurde dit in het
    toneelzaaltje van 'Het Harmonieke'. Daarna concerteerde de roomse harmonie op het marktveld als
    afsluiting, tenzij een feestelijke maaltijd ter afronding was voorbereid. In 1926 zocht Jules
    Froger naar 'ons Brabantse volk', en meer concreet naar iemand die als spreker op de Landdag
    richtingwijzend zou kunnen optreden. Toevallig stootte hij op Dr. P.C. de Brouwer, tot dan toe
    geheel onbekend voor hem. Dr. de Brouwer is sindsdien als moderator (geestelijk adviseur) aan
    het Gilde verbonden gebleven. Hoogstwaarschijnlijk heeft 'den doctor' aan de wieg gestaan van de
    naam die de studentenvereniging kreeg bij de eerste Landdag: 'het Brabants Studenten Gilde van
    Onze Lieve Vrouw'. Dat het Gilde onder de hoede kwam te staan van 'Onze Lieve Vrouw' kan
    toegeschreven worden aan het feit dat 'moeder de vrouw' de scepter zwaaide in het doorsnee
    Brabantse gezin. In die hoedanigheid heeft zij ervoor zorg gedragen dat Brabant in de tijd van
    Reformatie en retorsie zo goed als volledig katholiek is gebleven. Zo moet ook Dr. de Brouwer
    gedacht hebben. Voor Dr. de Brouwer was het Brabants Studenten Gilde een instrument om
    toekomstige intellectuelen te doordringen van zijn eigen ideaal: het Brabantse volk te helpen
    zijn minderwaardigheidsgevoel te overwinnen en zich bewust te worden van zijn eigenheid, zoals
    die in de loop der eeuwen was ontstaan. De eigen identiteit was af en toe misschien gevormd door
    geografische, ethnologische en historische factoren, meer wezenlijk echter door de gemeenschap
    vormende kracht van het Rooms-Katholieke geloof. Het Brabants Studenten Gilde won aan
    levenskracht door de opening van de Tilburgse Hogeschool in oktober 1927, al gauw gevolgd door
    het Tilburgs Studentencorps 'St. Olof'. De mores van een katholiek corps werden aan hen
    overgedragen door het Delftse corps, Jules Froger incluis. Uit dit vroege contact is het te
    verklaren dat de Landdag van 1928 al door 'Tilburg' werd georganiseerd. In Hilvarenbeek werd de
    Landdag voorafgegaan door heuse klassikale studiedagen over sociale vraagstukken. De
    Hilvarenbeekse Landdag kende een aanzienlijke opkomst. Aan de koffietafel zaten zo'n vijftig
    personen, waaronder de burgemeester, Dr. de Brouwer en Prof. Kaag met echtgenote. Zo'n
    honderdvijftig dorpelingen, inclusief twee toezichthoudende marechaussees, completeerden het
    geheel. Het Gilde was nog niet groot en is dat eigenlijk ook nooit geworden. Het was, althans
    objectief gezien, een allerbescheidenste manifestatie dat er een academische jeugd in Brabant
    begon op te komen (1).
  </p>
  <h2 class="my-2 text-xl font-bold">Intensivering en identiteit</h2>
  <p>
    De 'Tilburgse' Landdag in Hilvarenbeek wakkerde het enthousiasme voor het Gilde aan. Men ging
    streven naar een intensiever gildeleven, wat zich uitte in samenkomsten in de kerst- en
    paasvakanties voor de leden. Deze samenkomsten werden niet centraal, maar districtsgewijs in de
    diverse steden gehouden. Naar oude gilde-zede werd op tweede kerstdag en op tweede paasdag
    begonnen met een Heilige mis. Hierna werd gezamenlijk ontbeten. Het ontbijt werd gevolgd door
    een spreker van buiten of uit eigen kring, waarna weer koffie werd gedronken, pijp werd gerookt
    en over het gehoorde nageboomd. Zelfs in het licht van het lage aanbod aan Brabantse studenten
    moet men vaststellen dat het Gilde wel heel moeizaam op gang is gekomen, kijkend naar de foto
    van de koffietafel van de vijfde Landdag (wederom in Oirschot) in 1930. 34 deelnemers, onder wie
    Dr. de Brouwer en de Vlaamse pater van Sante O.P., die de rede zou houden, vergezelden de
    burgemeester en een paar leden van het comité uit de burgerij. Goed geteld waren er maar 25 lid
    van het Brabants Studenten Gilde. De -nog steeds kleine- groep werd echter wel steeds actiever.
    Voorafgaand aan de Land- dag werd voortaan een driedaags studiekamp gehouden, waarbij de
    deelnemers in een boerenschuur waren ondergebracht of in een eigen tent op het erf van de boer
    bivakkeerden. Om de geest even te ontspannen maakten ze tussendoor fietstochten en op de
    vooravond van de Landdag stookten ze een groot kampvuur, waaraan de mensen uit de naaste
    omgeving en vooral de kinderen opgetogen deelnamen. Zo'n kamp vond voor de eerste keer plaats in
    1929 te Zundert, voorafgaand aan de Landdag te Alphen. De tweede keer was in 1930 te Oirschot.
  </p>

  <h2 class="my-2 text-xl font-bold">Tweespalt en scheuring</h2>
  <p>
    Aansluitend op deze landdag vond er op 31 augustus van dat jaar een bijeenkomst plaats van de
    'gilde-leiders' met de moderator, teneinde de identiteit van het gilde ideologisch uit te
    bouwen. Dit resulteerde in een stuk van drie vellen dat niet bedoeld was als reglement, maar als
    vastlegging van enkele 'gegroeide' richtlijnen. Aan de leden werd een bewuste levenswandel naar
    de volgende vier gezichtspunten voorgehouden: het katholiek-zijn, het Brabander-zijn, het
    student-zijn en het gildebroeder-zijn (studenten waren nog altijd vooral jongens). Opvallend
    hierbij is dat het godsdienstige facet evenveel aandacht kreeg als de andere drie samen. Dit
    komt vooral doordat aan het katholiek-zijn een persoonlijke gedragsmoraal werd verbonden. Er
    werd gewaarschuwd tegen een materialistische levenswandel. De gildebroeders zouden bij zichzelf,
    als voorbeeld voor anderen, de genotzucht moeten bestrijden. 'Immers, in de geschiedenis volgde
    altijd op een periode van weelde en genotzucht het geestelijk verval, terwijl aan een periode
    van geestelijke opbloei altijd een periode van eenvoud, soberheid en zelfverloochening vooraf
    ging.' Wat het 'bewuste Brabanderschap' betreft, valt in de richtlijn de stelling op dat men
    'alle ongezond provincialisme' moest vermijden. In de frase over het 'gildebroeder-zijn' werd
    gerept over 'een echte eenvoudige broederlijke geest'. Een zinsnede uit de paragraaf over de
    verplichtingen als 'student' bevat twee tegenstrijdige elementen, die later een scheuring binnen
    het Gilde teweeg zou brengen. De studie van het vak was uiteraard de eerste plicht van de
    student geacht, maar hierop volgde meteen de uitspraak: 'daar zij krachtens hun studie tot de
    leidende standen van hun volk moeten gaan behoren zullen zij in hun studententijd de algemene
    beschavingsvraagstukken bestuderen, waardoor zij hun volk later met inzicht kunnen dienen.' De
    dienende kant van de komende intelligentia aan hun volk was het leidmotief van Dr. P.C. de
    Brouwer. Aangezien het lage banen-aanbod in het wat achtergebleven Brabant een brain-drain
    dreigde te veroorzaken, was het zaak de academici zich aan Brabant te laten verplichten.
    Daarnaast was er de stelling dat de academici vooral tot de 'leidende standen' behoorden. Er
    werd gerept over vertikale' en horizontale' standen. De vertikale beroeps-standen' behelsten
    alle medewerkers aan een produktielijn van grondstof tot eindprodukt. Ze werden gezien als de
    pijlers van de 'perfecte samenleving': de Staat. Deze pijlers dienden bijeengehouden te worden
    door het cement van de horizontale standen: arbeiders, burgerij, academici, geestelijkheid, etc.
    In de hier beschreven visie waren de katholieke academici de aangewezenen waren om de
    leidende-standsorganisaties in het leven te roepen (1)! De dienende rol werd naar achteren
    geschoven. Het verschil in benadering van de richtlijnen is aanvankelijk door niemand onderkend
    als een vonk waaruit een binnenbrandje zou kunnen ontstaan. Om te kunnen begrijpen dat ondanks
    alle (in de richtlijnen uitgesproken) verwachtingen van de 'oprechte broederlijkheid' een diepe
    kloof in het Gilde ontstond, moeten we even terug naar de uitwendige geschiedenis. Rond 1930
    verschenen naast De Brouwer twee priesterfiguren, die als twee antipoden kunnen worden
    beschouwd. Het waren antipoden van nature, die wegens hun geprononceerde eigenschappen en
    opvattingen voorbestemd leken volgelingen naar zich toe te trekken. Deze volgelingen konden
    elkaar op een bepaald moment niet meer verdragen en sloegen hun eigen weg in. De priesterfiguren
    hebben zelf nooit strijdend tegenover elkaar gestaan. De één was Pater R. van Sante O.P., die
    vanaf "Oirschot 1930" vooral via de Delftenaren contacten met het Gilde bleef houden, de ander
    was de collega en oud-congregatie-genoot van Dr. de Brouwer: Frans Siemer. Siemer was van
    'Leonardus', de studentenvereniging aan de Katholieke Leergangen. Via hem was er, ook voor het
    Gilde, een nieuwe culturele impuls ontstaan: in 1931 werd op de Landdag in Baarle-Nassau onder
    de (onzichtbare) regie van Siemer een openlucht-toneelspel opgevoerd: 'De gehangene met den
    koorde', van Henri Gh‚on. Pater van Sante, ook daar aanwezig, hield niet van toneelspelen en van
    spel in het algemeen. Hij was een ernstig filosoof en theoloog, hetgeen in die dagen op het-
    zelfde neer kwam. Waarom enkele gildeleden, vooral Delftenaren, hem mateloos vereerden, is niet
    echt duidelijk. Er werd over hem gesproken als dè pater, net zoals P.C. de Brouwer werd
    aangeduid met dèn doctor. Waarschijnlijk werd daarmee bedoeld Pater van Sante als concurrerend
    (of zelfs als enig) geestelijk gezag in het Gilde aan te merken. Van een tegenstelling in
    geloofsbeleving was in echter weinig sprake. De uitbundige' Siemer leefde uiterst matig, dronk
    geen druppel alcohol en had alles, ook in materiële zin, over voor anderen. Hij viel niemand
    lastig met persoonlijke principes, maar leerde zonder zedepreken de jonge mensen rond hem een
    'scone' leven te leiden. Mogelijk kwam het de 'Van Santianen' voor dat de levensstijl van de
    'Siemerianen' te overvloedig was en doelden zij met hun eigen soberheid op een ingetogen
    levenspatroon ter voorbereiding op de ernstige taken die hen in de toekomst wachtten. Het een
    feit dat een bloed-ernstige politieke richting als het 'Verdinaso' (Verbond van Dietsch
    Nationaal Solidaristen) binnen het Gilde aanhang heeft gekregen bij de partijgangers van Pater
    van Sante. Bij menig deelgenoot aan die groep leefde een oprecht idealisme, maar het fanatieke
    karakter ervan werkte wel op de zenuwen en de lachspieren van de levensluchtigere en een potje
    bier drinkende gildebroeders. Zij reageerden met opzettelijke luidruchtigheid en het zingen van
    spotliederen. In het kamp van 1932 schijnen zich ernstige moeilijkheden te hebben voorgedaan. De
    daarbij aansluitende Landdag in Gemert verliep nogal rustig. Op de Landdag van Huijbergen (1933)
    barstte de bom. De 'Van Santianen' traden definitief uit het Gilde. En nog wel op de Landdag
    waarop een nieuwe traditie een aanvang nam (1).
  </p>

  <h2 class="my-2 text-xl font-bold">Initiatief kapellenbouw</h2>
  <p class="mb-2">
    Op de Landdag in Huijbergen werd de eerste door gildebroeders tijdens het kamp gebouwde
    Mariakapel ingezegend. Deze kapellenbouw, waardoor het Gilde in de daarop volgende jaren enige
    naambekendheid kreeg, is niet een initiatief van de officiële gildeleiding geweest. Vermoedelijk
    heeft Siemer er wel de hand in gehad. Hij kwam geregeld bij de familie Bedaux, die een
    mini-Mariakapel in de hof van het ouderlijk huis had gebouwd. Siemer heeft waarschijnlijk bij
    het bestuur aangekaart om tijdens het kamp van het Gilde iets dergelijks te bouwen. De eerste
    kapel in Huijbergen was een vergroot duplicaat van de Bedaux kapel, ook nu weer gebouwd door
    dezelfde leden van de familie Bedaux. Deze activiteit heeft zoveel enthousiasme opgewekt onder
    de gildebroeders, dat werd besloten zo mogelijk elk jaar een kapel te bouwen. Tot in de oorlog
    toe, zij het toen op afgelegen plaatsen door onderduikers, is dit inderdaad ieder jaar gebeurd.
    De ontwerpen werden iedere keer aangeboden door de architect Bedaux. Toen in 1945 de oorlog
    voorbij was, werd in Oosterhout de traditie met zo'n grote kapel hernomen, dat ze niet door
    studenten gebouwd kon worden, maar moest worden uitbesteed. Nadien is de traditie echter op de
    gebruikelijke manier hervat.
  </p>
